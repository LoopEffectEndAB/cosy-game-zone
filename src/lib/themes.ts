const THEME_KEY = "nexus-theme";

export function getStoredTheme(): string {
  return localStorage.getItem(THEME_KEY) || "gaming";
}

export function setStoredTheme(id: string) {
  localStorage.setItem(THEME_KEY, id);
}

export function applyTheme(id: string) {
  const theme = themes.find((t) => t.id === id);
  if (!theme) return;
  const root = document.documentElement;
  Object.entries(theme.variables).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });
  // Update gradient variables that depend on neon colors
  root.style.setProperty(
    "--gradient-gaming",
    `linear-gradient(135deg, hsl(${theme.variables["--neon-cyan"]}) 0%, hsl(${theme.variables["--neon-magenta"]}) 50%, hsl(${theme.variables["--neon-purple"]}) 100%)`
  );
  root.style.setProperty(
    "--glow-cyan",
    `0 0 20px hsl(${theme.variables["--neon-cyan"]} / 0.5), 0 0 40px hsl(${theme.variables["--neon-cyan"]} / 0.3)`
  );
  root.style.setProperty(
    "--glow-magenta",
    `0 0 20px hsl(${theme.variables["--neon-magenta"]} / 0.5), 0 0 40px hsl(${theme.variables["--neon-magenta"]} / 0.3)`
  );
  root.style.setProperty(
    "--glow-purple",
    `0 0 20px hsl(${theme.variables["--neon-purple"]} / 0.5), 0 0 40px hsl(${theme.variables["--neon-purple"]} / 0.3)`
  );
  root.style.setProperty(
    "--glass-bg",
    `hsl(${theme.variables["--card"]} / 0.8)`
  );
  root.style.setProperty(
    "--glass-border",
    `hsl(${theme.variables["--border"]} / 0.5)`
  );
  setStoredTheme(id);
}

// Apply saved theme on load
export function initTheme() {
  applyTheme(getStoredTheme());
}
